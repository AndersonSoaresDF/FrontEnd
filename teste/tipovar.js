var numero = 5.2; // Tipo Number (1,10, 2.5, 5.3050).
console.log(numero); // Typeof(numero).
console.log(typeof(numero)); // Usa o operador typeof para identificar o tipo da variável numero. Resultado: "number".


var palavra = "JavaScript"; // Tipo String (Cadeia de Caracteres).
console.log(palavra); // Exibe o valor de palavra. Resultado: "JavaScript".
console.log(typeof(palavra)); // Mostra o tipo da variável palavra. Resultado: "string".


var logica = false; // Tipo Booleano true (Verdadeiro) ou false (falso). Declara a variável logica com o valor false, do tipo booleano, que pode ser true ou false.
console.log(logica); // Exibe o valor da variável logica. Resultado: false.
console.log(typeof(logica)); // Mostra o tipo da variável logica.  Resultado: "boolean"

var qualquer; // Declara a variável qualquer sem atribuição de valor. O JavaScript define variáveis sem valor como undefined.
console.log(qualquer); // Exibe o valor da variável qualquer. Resultado: undefined.
console.log(typeof(qualquer)); // Mostra o tipo da variável qualquer. Resultado: "undefined".

